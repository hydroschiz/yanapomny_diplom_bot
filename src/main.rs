//! # YaPomnyu Bot - Telegram Reminder Bot
//!
//! Telegram бот для создания и управления напоминаниями с использованием
//! естественного языка и LLM для парсинга.
//!
//! ## Архитектура
//!
//! ```text
//! ┌─────────────────────────────────────────────────────────────┐
//! │                        main.rs                               │
//! │  Точка входа: инициализация логирования и запуск app::run() │
//! └─────────────────────────────────────────────────────────────┘
//!                              │
//!          ┌───────────────────┼───────────────────┐
//!          ▼                   ▼                   ▼
//!    ┌──────────┐       ┌──────────┐        ┌──────────┐
//!    │   bot/   │       │   api/   │        │scheduler/│
//!    │ Telegram │       │ MongoDB  │        │  Фоновая │
//!    │ handlers │       │ LLM API  │        │  задача  │
//!    └──────────┘       └──────────┘        └──────────┘
//! ```
//!
//! ## Модули
//!
//! - [`api`] - Работа с внешними сервисами (MongoDB, Redis, LLM API, YooKassa)
//! - [`app`] - Инициализация и запуск приложения
//! - [`bot`] - Telegram бот (handlers, states, keyboards)
//! - [`config`] - Конфигурация из переменных окружения
//! - [`scheduler`] - Фоновый планировщик отправки напоминаний
//! - [`utils`] - Вспомогательные утилиты

mod api;
mod app;
mod bot;
mod config;
mod scheduler;
mod utils;

/// Точка входа приложения.
///
/// Выполняет:
/// 1. Загрузку переменных окружения из `.env` файла
/// 2. Инициализацию structured logging с фильтром из `RUST_LOG`
/// 3. Запуск основного приложения через [`app::run`]
///
/// # Errors
///
/// Возвращает ошибку если:
/// - Не удалось подключиться к MongoDB
/// - Не удалось запустить Telegram бот
/// - Любая критическая ошибка инициализации
#[tokio::main]
async fn main() -> anyhow::Result<()> {
    // Загружаем .env файл (игнорируем ошибку если файл отсутствует)
    dotenvy::dotenv().ok();

    // Инициализируем structured logging
    // Уровень логирования берётся из RUST_LOG (например: RUST_LOG=info)
    tracing_subscriber::fmt()
        .with_env_filter(tracing_subscriber::EnvFilter::from_default_env())
        .init();

    // Запускаем основное приложение
    app::run().await
}
